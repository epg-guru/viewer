// Minimal offline-first service worker for the app shell. __BUILD_HASH__ is
// replaced with the commit hash at build time (see vite.config.ts) so each
// deploy gets a fresh cache name and the activate handler purges the old one.
const CACHE = "epgv-__BUILD_HASH__";

self.addEventListener("install", () => {
  self.skipWaiting();
});

self.addEventListener("activate", (event) => {
  event.waitUntil(
    caches
      .keys()
      .then((keys) => Promise.all(keys.filter((k) => k !== CACHE).map((k) => caches.delete(k))))
      .then(() => self.clients.claim()),
  );
});

self.addEventListener("fetch", (event) => {
  const { request } = event;
  if (request.method !== "GET") return;

  const url = new URL(request.url);
  // Only handle our own origin/shell assets. EPG source fetches (arbitrary
  // cross-origin XMLTV URLs, often huge) are handled entirely by the app's
  // own parser worker and must never pass through this cache.
  if (url.origin !== self.location.origin) return;

  event.respondWith(
    caches.match(request).then((cached) => {
      const network = fetch(request)
        .then((response) => {
          if (response.ok) {
            const copy = response.clone();
            caches.open(CACHE).then((cache) => cache.put(request, copy));
          }
          return response;
        })
        .catch(() => cached);
      return cached || network;
    }),
  );
});
